#ifndef __SIZES_H__
#define __SIZES_H__

#include <stdbool.h>

#define SIZEOF_BOOL                 sizeof(bool)
#define SIZEOF_CHAR                 sizeof(char)
#define SIZEOF_UNSIGNED_CHAR        sizeof(unsigned char)
#define SIZEOF_SHORT                sizeof(short)
#define SIZEOF_UNSIGNED_SHORT       sizeof(unsigned short)
#define SIZEOF_INT                  sizeof(int)
#define SIZEOF_UNSIGNED_INT         sizeof(unsigned int)
#define SIZEOF_LONG                 sizeof(long)
#define SIZEOF_UNSIGNED_LONG        sizeof(unsigned long)
#define SIZEOF_LONG_LONG            sizeof(long long)
#define SIZEOF_UNSIGNED_LONG_LONG   sizeof(unsigned long long)
#define SIZEOF_FLOAT                sizeof(float)
#define SIZEOF_DOUBLE               sizeof(double)
#define SIZEOF_LONG_DOUBLE          sizeof(long double)
#define SIZEOF_CHAR_POINTER         sizeof(char *)
#define SIZEOF_INT_POINTER          sizeof(int *)
#define SIZEOF_DOUBLE_POINTER       sizeof(double *)
#define SIZEOF_VOID_POINTER         sizeof(void *)

#endif // __SIZES_H__